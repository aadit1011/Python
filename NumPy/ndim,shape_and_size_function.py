import numpy as np  # Importing the numpy library for numerical computations and array operations.

# Creating a 3D NumPy array.
arr3d = np.array([
                [[1, 2, 3],
                [4, 5, 6]]
                ])

# Printing the original 3D array.
print(f'The original 3D array = {arr3d}')
print()  # Print an empty line for readability.

# Printing the size of the 3D array (total number of elements).
print(f'The size of 3D array is {arr3d.size}')
print()  # Print an empty line for readability.

# Printing the number of dimensions of the 3D array.
print(f'The dimension of array is {arr3d.ndim}')
print()  # Print an empty line for readability.

# Printing the shape of the 3D array (dimensions of the array).
print(f'The shape of array is {arr3d.shape}')
print()  # Print an empty line for readability.

